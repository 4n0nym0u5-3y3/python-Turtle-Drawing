# Function to draw a single spiral
def draw_spiral(x_start, angle, increment, color_step):
    x = x_start
    c = 0
    while x < 1000:
        idx = int(c) % len(colors)  # Use modulo to cycle through colors
        color = colors[idx]
        turtle.color(color)
        turtle.forward(x)
        turtle.right(angle)
        x += increment
        c += color_step

# Draw multiple spirals with varying parameters
for i in range(5):
    turtle.penup()
    turtle.goto(-50 * i, 0)  # Offset each spiral
    turtle.pendown()
    draw_spiral(x_start=1, angle=98, increment=2, color_step=0.2 + i * 0.05)

turtle.hideturtle()
turtle.done()
